function populateAgeOptions() {
    let ageSelect = document.getElementById('age'); // Get the age select element

    // Loop through the desired age range and create options
    for (let age = 18; age <= 90; age++) {
        let option = document.createElement('option'); // Create a new option element
        option.value = age; // Set the option's value attribute
        option.textContent = age; // Set the text content of the option
        ageSelect.appendChild(option); // Append the option to the select element
    }
}

